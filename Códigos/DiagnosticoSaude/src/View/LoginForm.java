    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:

        String login = txtLogin.getText();
        String senha = null;
        List<Enfermeiro> lista = null;
        try {
            senha = Hash.getMD5(new String(txtSenha.getPassword()));
        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Problema ao gerar hash !");
        }

        try {
            lista = dao.searchBy(new String[]{"login", "senha"}, new String[]{login, senha});
        } catch (SQLException ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Problema ao pesquisar no banco de dados !");
        }
        if (!lista.isEmpty()) {
            Enfermeiro enfermeiro = lista.get(0);
            Session.saveUser(enfermeiro);
            this.setVisible(false);

            new MainForm(this).setVisible(true);

        } else {
            txtSenha.setText("");
            txtLogin.setText("");
            txtLogin.requestFocus();
        }

    }//GEN-LAST:event_btnLoginActionPerformed

